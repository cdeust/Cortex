# ── Drawing helpers ─────────────────────────────────────────────────────


def ease(t: float) -> float:
    return t * t * (3 - 2 * t)


def fade_in(f: int, start: int, dur: int) -> float:
    if f < start:
        return 0.0
    if f >= start + dur:
        return 1.0
    return ease((f - start) / dur)


def fade_out(f: int, start: int, dur: int) -> float:
    return 1.0 - fade_in(f, start, dur)


def fade_io(f: int, start: int, hold: int, fade: int) -> float:
    return min(fade_in(f, start, fade), fade_out(f, start + fade + hold, fade))


def text_center(img, text, y, font, color, alpha):
    if alpha <= 0:
        return
    ov = Image.new("RGBA", img.size, (0, 0, 0, 0))
    d = ImageDraw.Draw(ov)
    r, g, b = color
    bb = d.textbbox((0, 0), text, font=font)
    x = (WIDTH - (bb[2] - bb[0])) // 2
    d.text((x, y), text, font=font, fill=(r, g, b, int(alpha * 255)))
    img.paste(Image.alpha_composite(img.convert("RGBA"), ov).convert("RGB"))


class Particle:
    def __init__(self, x, y, vx, vy, life, color, size):
        self.x, self.y = x, y
        self.vx, self.vy = vx, vy
        self.life = self.max_life = life
        self.color, self.size = color, size

    def update(self):
        self.x += self.vx
        self.y += self.vy
        self.life -= 0.012

    @property
    def a(self):
        return max(0, self.life / self.max_life)

    @property
    def alive(self):
        return self.life > 0


class Particles:
    def __init__(self):
        self.ps = []

    def add(self, p):
        self.ps.append(p)

    def update(self):
        for p in self.ps:
            p.update()
        self.ps = [p for p in self.ps if p.alive]

    def draw(self, draw):
        for p in self.ps:
            a = p.a
            r, g, b = p.color
            c = (int(r * a), int(g * a), int(b * a))
            s = p.size * a
            draw.ellipse([p.x - s, p.y - s, p.x + s, p.y + s], fill=c)


