def main():
    if FRAME_DIR.exists():
        shutil.rmtree(FRAME_DIR)
    FRAME_DIR.mkdir(parents=True)
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    parts = Particles()

    print(f"Generating {TOTAL} frames ({TOTAL / FPS:.0f}s at {FPS}fps)...")

    for f in range(TOTAL):
        if f < 900:
            img = render_beat1(f, parts)
        elif f < 1500:
            img = render_beat2(f, parts)
        elif f < 2400:
            img = render_beat3(f, parts)
        elif f < 3300:
            img = render_beat4(f, parts)
        else:
            img = render_beat5(f, parts)

        img.save(FRAME_DIR / f"frame_{f:05d}.png")
        if f % 200 == 0:
            beat = (
                1
                if f < 900
                else (2 if f < 1500 else (3 if f < 2400 else (4 if f < 3300 else 5)))
            )
            print(f"  [{f / TOTAL * 100:5.1f}%] Frame {f} (Beat {beat})")

    print("Assembling with ffmpeg...")
    subprocess.run(
        [
            "ffmpeg",
            "-y",
            "-framerate",
            str(FPS),
            "-i",
            str(FRAME_DIR / "frame_%05d.png"),
            "-c:v",
            "libx264",
            "-pix_fmt",
            "yuv420p",
            "-crf",
            "18",
            "-preset",
            "slow",
            str(OUTPUT_FILE),
        ],
        check=True,
    )

    print(f"\nDone: {OUTPUT_FILE} ({TOTAL / FPS:.0f}s)")
    shutil.rmtree(FRAME_DIR)


if __name__ == "__main__":
    main()
